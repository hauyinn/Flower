     public String createPromotionProductID(){
         PromotionProductDA ppDA = new PromotionProductDA();
        List<Promotionproduct> pList=new ArrayList<Promotionproduct>();
        String ppID = null;
        char idCore;
         pList=ppDA.getAllRecord();
        for (Promotionproduct a: pList){
            ppID=a.getPromotiomitemid();
        }
        if(ppID==null){
            ppID="P000";
        }
        idCore=ppID.charAt(0);
        int hun=Character.getNumericValue(ppID.charAt(1));
        int ten=Character.getNumericValue(ppID.charAt(2));
        int one=Character.getNumericValue(ppID.charAt(3));
        one++;
        if(one==10){
            ten++;
            one=0;
        }
        if(ten==10){
            hun++;
            ten=0;
        }
        return idCore+Integer.toString(hun)+Integer.toString(ten)+Integer.toString(one);
    }
    
